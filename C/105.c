#include <stdio.h>

int main() 
{
    int i;

    printf("Таблица квадратов первых десяти целых положительных чисел:\n");
    printf("--------------------------------\n");
    printf("|   Число   |   Квадрат  |\n");
    printf("--------------------------------\n");

    for (i = 1; i <= 10; i++) 
	{
        printf("|     %2d    |    %4d     |\n", i, i * i);
    }

    return 0;
}

