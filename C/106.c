#include <stdio.h>

int main() {
    printf("Таблица квадратов первых пяти целых положительных нечетных чисел:\n");
    printf("--------------------------------------------\n");
    printf("|   Число   |   Квадрат  |\n");
    printf("--------------------------------------------\n");

    for (int i = 1; i <= 5; i++) {
        int odd_num = 2 * i - 1;
        printf("|     %2d    |    %4d     |\n", odd_num, odd_num * odd_num);
    }

    return 0;
}
